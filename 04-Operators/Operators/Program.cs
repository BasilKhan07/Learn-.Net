namespace Operators
{
    class Program
    {
        static void Main(string[] args)
        {
            var a = 4;
            var b = 2;

            Console.WriteLine(a + b);
            Console.WriteLine(a - b);
            Console.WriteLine(a * b);
            Console.WriteLine(a / b);
            Console.WriteLine(a % b);

            Console.WriteLine(a > b);
            Console.WriteLine(a >= b);  
            Console.WriteLine(a <= b);
            Console.WriteLine(a == b);
            Console.WriteLine(a != b);
        }
    }
}